export const coaches = pgTable(
  'coaches',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    slug: varchar('slug', { length: 80 }).notNull().unique(),
    name: varchar('name', { length: 80 }).notNull(),
    title: varchar('title', { length: 120 }).notNull(),
    bio: text('bio').notNull(),
    certifications: text('certifications').array(),
    specialties: text('specialties').array(),
    signatureWorkout: varchar('signature_workout', { length: 120 }),
    portraitKey: varchar('portrait_key', { length: 200 }),
    yearsExp: integer('years_exp'),
    order: integer('order').default(0).notNull(),
    published: boolean('published').default(false).notNull(),
    createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
    updatedAt: timestamp('updated_at', { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({ slugIdx: index('coaches_slug_idx').on(t.slug) }),
);

export const programs = pgTable(
  'programs',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    slug: varchar('slug', { length: 80 }).notNull().unique(),
    goal: varchar('goal', { length: 40 }).notNull(),
    title: varchar('title', { length: 120 }).notNull(),
    subtitle: varchar('subtitle', { length: 200 }),
    description: text('description').notNull(),
    duration: varchar('duration', { length: 40 }),
    sessionsPerWeek: integer('sessions_per_week'),
    intensity: varchar('intensity', { length: 20 }),
    heroKey: varchar('hero_key', { length: 200 }),
    priceCents: integer('price_cents'),
    stripePriceId: varchar('stripe_price_id', { length: 80 }),
    coachId: uuid('coach_id').references(() => coaches.id),
    order: integer('order').default(0).notNull(),
    published: boolean('published').default(false).notNull(),
    createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
    updatedAt: timestamp('updated_at', { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({ goalIdx: index('programs_goal_idx').on(t.goal) }),
);

export const stories = pgTable('stories', {
  id: uuid('id').primaryKey().defaultRandom(),
  slug: varchar('slug', { length: 80 }).notNull().unique(),
  memberName: varchar('member_name', { length: 80 }).notNull(),
  memberAge: integer('member_age'),
  programSlug: varchar('program_slug', { length: 80 }),
  weeks: integer('weeks'),
  beforeKey: varchar('before_key', { length: 200 }),
  afterKey: varchar('after_key', { length: 200 }),
  quote: text('quote').notNull(),
  timeline: jsonb('timeline'),
  videoKey: varchar('video_key', { length: 200 }),
  published: boolean('published').default(false).notNull(),
  createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
});

export const classSlots = pgTable(
  'class_slots',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    programId: uuid('program_id').references(() => programs.id),
    coachId: uuid('coach_id').references(() => coaches.id),
    startsAt: timestamp('starts_at', { withTimezone: true }).notNull(),
    durationMin: integer('duration_min').default(60),
    capacity: integer('capacity').default(8),
    location: varchar('location', { length: 120 }).default('47 Eastbound Alley, NYC'),
  },
  (t) => ({ startsAtIdx: index('class_slots_starts_at_idx').on(t.startsAt) }),
);

export const trialRequests = pgTable(
  'trial_requests',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    name: varchar('name', { length: 80 }).notNull(),
    email: varchar('email', { length: 160 }).notNull(),
    phone: varchar('phone', { length: 40 }),
    goal: varchar('goal', { length: 40 }).notNull(),
    preferredTime: varchar('preferred_time', { length: 20 }).notNull(),
    preferredCoachId: uuid('preferred_coach_id').references(() => coaches.id),
    notes: text('notes'),
    status: varchar('status', { length: 20 }).default('received'),
    idempotencyKey: varchar('idempotency_key', { length: 60 }).notNull().unique(),
    createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({ createdIdx: index('trial_requests_created_idx').on(t.createdAt) }),
);

export const newsletterSubs = pgTable(
  'newsletter_subs',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    email: varchar('email', { length: 160 }).notNull().unique(),
    consentAt: timestamp('consent_at', { withTimezone: true }).defaultNow().notNull(),
    source: varchar('source', { length: 40 }).default('site_footer'),
  },
);

// ── Subscriptions (Stripe membership billing) ──
export const subscriptionStatusEnum = pgEnum('subscription_status', [
  'active',
  'past_due',
  'canceled',
  'incomplete',
  'trialing',
]);

export const subscriptions = pgTable(
  'subscriptions',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    userId: uuid('user_id')
      .notNull()
      .references(() => users.id, { onDelete: 'cascade' }),
    stripeCustomerId: varchar('stripe_customer_id', { length: 100 }).notNull(),
    stripeSubscriptionId: varchar('stripe_subscription_id', { length: 100 }).notNull().unique(),
    stripePriceId: varchar('stripe_price_id', { length: 100 }).notNull(),
    tier: varchar('tier', { length: 40 }).notNull(), // 'forge' | 'forge_plus' | 'forge_private'
    status: subscriptionStatusEnum('status').notNull().default('active'),
    currentPeriodEnd: timestamp('current_period_end', { withTimezone: true }),
    cancelAtPeriodEnd: boolean('cancel_at_period_end').default(false),
    createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
    updatedAt: timestamp('updated_at', { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({
    customerIdx: index('subscriptions_customer_idx').on(t.stripeCustomerId),
    userIdx: index('subscriptions_user_idx').on(t.userId),
  }),
);

