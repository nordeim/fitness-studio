// ── Auth.js v5 enums ──
export const userRoleEnum = pgEnum('user_role', ['member', 'admin']);

// ── Auth.js v5 + @auth/drizzle-adapter tables ──
export const users = pgTable(
  'users',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    name: varchar('name', { length: 80 }),
    email: varchar('email', { length: 160 }).notNull().unique(),
    emailVerified: timestamp('email_verified', { withTimezone: true }),
    image: varchar('image', { length: 500 }),
    passwordHash: varchar('password_hash', { length: 100 }), // bcrypt — null for OAuth-only users
    role: userRoleEnum('role').notNull().default('member'),
    createdAt: timestamp('created_at', { withTimezone: true }).defaultNow().notNull(),
    updatedAt: timestamp('updated_at', { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({ emailIdx: index('users_email_idx').on(t.email) }),
);

export const accounts = pgTable(
  'accounts',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    userId: uuid('user_id')
      .notNull()
      .references(() => users.id, { onDelete: 'cascade' }),
    type: varchar('type', { length: 40 }).notNull(),
    provider: varchar('provider', { length: 40 }).notNull(),
    providerAccountId: varchar('provider_account_id', { length: 100 }).notNull(),
    refresh_token: text('refresh_token'),
    access_token: text('access_token'),
    expires_at: integer('expires_at'),
    token_type: varchar('token_type', { length: 40 }),
    scope: varchar('scope', { length: 200 }),
    id_token: text('id_token'),
    session_state: varchar('session_state', { length: 200 }),
  },
  (t) => ({
    providerIdx: index('accounts_provider_idx').on(t.provider, t.providerAccountId),
    userIdx: index('accounts_user_idx').on(t.userId),
  }),
);

export const sessions = pgTable(
  'sessions',
  {
    id: uuid('id').primaryKey().defaultRandom(),
    sessionToken: varchar('session_token', { length: 100 }).notNull().unique(),
    userId: uuid('user_id')
      .notNull()
      .references(() => users.id, { onDelete: 'cascade' }),
    expires: timestamp('expires', { withTimezone: true }).notNull(),
  },
  (t) => ({ userIdx: index('sessions_user_idx').on(t.userId) }),
);

export const verificationTokens = pgTable(
  'verification_tokens',
  {
    identifier: varchar('identifier', { length: 160 }).notNull(),
    token: varchar('token', { length: 100 }).notNull().unique(),
    expires: timestamp('expires', { withTimezone: true }).notNull(),
  },
  (t) => ({ identifierIdx: index('vt_identifier_idx').on(t.identifier) }),
);

// ──────────────────────────────────────────────────────────────────────
// Phase 5 tables (declared now so the schema barrel is stable).
// Migrations are generated by `pnpm drizzle:generate` once Phase 5 lands.
